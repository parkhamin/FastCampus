public class FourArithmetic {
    public static void main(String[] args) {
        // 두 개의 정수를 사칙연산(+,-,*,/)하여 출력하는 JavaSE 프로그램을 만들어 보자
        int a = 12;
        int b = 2;
        System.out.println("a + b = "+ (a+b));
        System.out.println("a + b = "+ (a-b));
        System.out.println("a + b = "+ (a*b));
        System.out.println("a + b = "+ (a/b));
    }
}
